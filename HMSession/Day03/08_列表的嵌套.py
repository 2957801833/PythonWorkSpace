"""
列表的嵌套： 就是 列表中的数据 还可以是列表

[  数据，数据2，数据3，... ]
数据 可以是任意的数据
[  [],[],[] ]
"""
# 1. 定义嵌套的列表
data_list = [
    [1,"张三","测试"],
    [2,"李四","测试开发"],
    [3,"王五","测试经理"]
]
# 2. 获取嵌套的数据
# 列表变量名[索引]
# 一行数据 = data_list[1]
lisi_data = data_list[1]
lisi = lisi_data[1]
print(lisi)
# lisi = data_list[1][1]
print(   data_list[1][1] )