# 变量
# 变量名 = 数据值
name = "唐伯虎"
age = 20
# 打印
# 问题是： 打印了name -- 唐伯虎 在一行
# 打印了 age -- 20 在一行
# end='\n'  end 结束 打印了之后 print会自动的添加一个 \n
print(name)
print(age)
# \ 是 enter 键上边的
# \ 拥有神奇的魔力 能够配合斜杠后边的单词 产生特殊的效果
# 例如 \n 就是换行
# \n 就是转义   转变原来的含义 \n 就是换行
print("床前明月光 \n 疑是地上霜")
# \t 就是制表符【就是一个 Tab键 就是键盘左边的Tab Q左边的这个键】
print("姓名\t\t\t年龄\t住址\t爱好")
