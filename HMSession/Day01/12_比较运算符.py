num1 = 5
num2 = 2
# 5 大于 2  是 真的  True [必须这样写 T大写 而且 True是python关键字 ]
print(  num1 > num2 )
# 5 小于 2 是 假的  False [必须这样写 F大写 而且 False是Python关键字]
print(  num1 < num2 )
# True 或者 False 就2个值 我们称之为 布尔类型
# 基本数据类型： 整形，浮点数，字符串，布尔型
# 是否相等  是2个等于号   2个等于号是进行比较
print(  num1 == num2 )

# 类型都不一致
# 是否为真
print(  1 == "1" )