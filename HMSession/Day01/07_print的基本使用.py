"""
输出： 就是理解为 把数据 通过 print 打印出来 ，在控制台显示
print(  数据值/变量名 )
print() 里边可以跟很多的变量或者数据，变量和数据直接用逗号分隔
print(数据值/变量名,数据值/变量名,... )
"""
# 1 是整数
print(1)
# "1" 是字符串
print("1")
# 类型不一样  1 是整数 不是 变量名
# 变量名 遵循 标识符命名规则
# 定义一个变量
# 变量名 = 数据值
name = "黑马程序员"
print(name)
print("name")
# 有引号 和 没有引号 不一样！！！！
age = 20
like = "踢足球"
print(name , age , like , "你呢？")
