#导入turtle模块
import turtle
#显示Turtle的当前位置和方向
turtle.showturtle()
#绘制文本字符串
turtle.write("Welcome to python")
#将箭头向前移动100个像素
turtle.forward(100)
#将箭头右转90度
turtle.right(90)
#将颜色改为红色
turtle.color("red")
#向前移动50个像素
turtle.forward(50)
#箭头右转90度
turtle.right(90)
#颜色修改为绿色
turtle.color("green")
#向前移动100个像素
turtle.forward(100)
#箭头右转45度
turtle.right(45)
# 向前移动80个像素
turtle.forward(80)

turtle.done()